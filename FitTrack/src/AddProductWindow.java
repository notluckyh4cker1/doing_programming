    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField1.getText().trim();
        try {
            double proteins = Double.parseDouble(jTextField2.getText().trim());
            double fats = Double.parseDouble(jTextField3.getText().trim());
            double carbs = Double.parseDouble(jTextField4.getText().trim());
            double calories = Double.parseDouble(jTextField5.getText().trim());

            AddProductConnector newprod = new AddProductConnector();
            boolean success = newprod.addProduct(name, calories, proteins, fats, carbs);

            if (success) {
                javax.swing.JOptionPane.showMessageDialog(this, "Продукт успешно добавлен!");
                dispose();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Ошибка при добавлении продукта!");
            }
        } catch (NumberFormatException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Некорректный ввод чисел.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

